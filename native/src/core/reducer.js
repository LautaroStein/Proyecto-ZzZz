export const initialState = {
    text:"HOLA DESE REDUCER",
  
}

export const actionTypes = {
    TEXT:"TEXT",
  
}




const reducer =(state, action ) =>{

    console.log(action);
    
 
switch(action.type){
  
        case "TEXT":
            return{
                ...state,
                text:action.text,
            }
                                                                                    
        default: return state;
}
}
export default reducer